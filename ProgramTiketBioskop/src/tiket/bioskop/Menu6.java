    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:                                   
    int selectedRow = jTable1.getSelectedRow();
    if (selectedRow != -1) {
        // Ambil nilai dari kolom-kolom yang sesuai dari model tabel
        String noTelp = jTable1.getValueAt(selectedRow, 0).toString();
        String jamTayang = jTable1.getValueAt(selectedRow, 1).toString();
        String film = jTable1.getValueAt(selectedRow, 2).toString();
        String hari = jTable1.getValueAt(selectedRow, 3).toString();
        String jumlah = jTable1.getValueAt(selectedRow, 4).toString();
        String kursi = jTable1.getValueAt(selectedRow, 5).toString();
        String harga = jTable1.getValueAt(selectedRow, 6).toString();
        String tanggal = jTable1.getValueAt(selectedRow, 7).toString();
        String studio = jTable1.getValueAt(selectedRow, 8).toString();

        // Mengatur nilai ke label dan combo box yang sesuai
        txtIDPesan.setText(noTelp);
        cbJam.setSelectedItem(jamTayang);
        cbFilm.setSelectedItem(film);
        cbHari.setSelectedItem(hari);
        txtJumlah.setText(jumlah);
        txtKursi.setText(kursi);
        txtHarga.setText(harga);
        txtTanggal.setText(tanggal);
        txtStudio.setText(studio);
    }


    }//GEN-LAST:event_jTable1MouseClicked

