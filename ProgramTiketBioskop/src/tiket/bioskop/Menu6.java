    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        try {
    String noTelp = txtIDPesan.getText();
    
    if (noTelp.isEmpty()) {
        JOptionPane.showMessageDialog(null, "ID Pesan tidak boleh kosong");
    } else {
        java.sql.Connection conn = (java.sql.Connection) tiket.bioskop.Koneksi.getKoneksi();
        String sql = "DELETE FROM tbl_pesan WHERE id_pesan = ?";
        java.sql.PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, noTelp);
        int result = pst.executeUpdate();
        
        if (result > 0) {
            JOptionPane.showMessageDialog(null, "Data berhasil dihapus");
            tampil_data();
            bersih();
        } else {
            JOptionPane.showMessageDialog(null, "Gagal menghapus data");
        }
    }
} catch (Exception e) {
    JOptionPane.showMessageDialog(null, "Proses Penghapusan Gagal");
    System.out.println(e.getMessage());
}

    }//GEN-LAST:event_jButton4ActionPerformed

