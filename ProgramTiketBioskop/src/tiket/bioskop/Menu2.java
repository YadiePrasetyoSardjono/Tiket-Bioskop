    public void id_pesan_otomatis() {
    try {
        // Mendapatkan koneksi dari kelas Koneksi
            Connection connection = tiket.bioskop.Koneksi.getKoneksi();
            String query = "SELECT * FROM tbl_pesan ORDER BY id_pesan DESC";
            PreparedStatement statement = connection.prepareStatement(query);
            ResultSet resultSet = statement.executeQuery();
            if (resultSet.next()) {
                String id = resultSet.getString(1);
                int nextId = Integer.parseInt(id.substring(1)) + 1;
                String AN = String.format("%03d", nextId);
                txtidPesan.setText("P" + AN);
            } else {
                txtidPesan.setText("P001");
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
    
