    public void hitung(){
        if (cbHari.getSelectedItem().equals("Pilih Hari")){
        txtHarga.setText("");
        } else {
        String jumlahTiketText = LabelJumlahTiket.getText();
        if (jumlahTiketText.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Mohon pilih kursi terlebih dahulu.");
        } else {
            int jumlahKursi = Integer.parseInt(jumlahTiketText);
            int totalHarga = 0;
            if (cbHari.getSelectedItem().equals("Senin") ||
                cbHari.getSelectedItem().equals("Selasa") ||
                cbHari.getSelectedItem().equals("Rabu") ||
                cbHari.getSelectedItem().equals("Kamis")) {
                totalHarga = jumlahKursi * 50000;
            } else if (cbHari.getSelectedItem().equals("Jumat")) {
                totalHarga = jumlahKursi * 60000;
            } else if (cbHari.getSelectedItem().equals("Sabtu") ||
                       cbHari.getSelectedItem().equals("Minggu")) {
                totalHarga = jumlahKursi * 70000;
            }
            txtHarga.setText(String.valueOf(totalHarga));
        }
        }
    }
