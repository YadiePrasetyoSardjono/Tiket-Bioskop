    private void batalkanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_batalkanActionPerformed
        // TODO add your handling code here:
        try {
            String notelp = txtIDPesan.getText();

    if (notelp.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Nomor Id Pesan harus diisi terlebih dahulu");
    } else {
        java.sql.Connection conn = (java.sql.Connection) tiket.bioskop.Koneksi.getKoneksi();
        String sql = "DELETE FROM tbl_pesan WHERE id_pesan=?";
        java.sql.PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, notelp);
        int result = pst.executeUpdate();

        if (result > 0) {
            JOptionPane.showMessageDialog(null, "Pesanan berhasil dibatalkan");
            bersih();
            Menu2 su = new Menu2();
            su.show();
            dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Proses pembatalan gagal");
        }
    }
} catch (HeadlessException | SQLException e) {
    JOptionPane.showMessageDialog(null, "Proses Pembatalan Gagal");
    System.out.println(e.getMessage());
}


    }//GEN-LAST:event_batalkanActionPerformed

