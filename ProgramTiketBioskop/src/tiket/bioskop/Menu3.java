    public void tampil_data() {
    try {
        java.sql.Connection conn = tiket.bioskop.Koneksi.getKoneksi();
        //String no = txtIDPesan.getText();
        //String sql = "SELECT jam_tayang, film, hari, jumlah_tiket, kursi, harga, tanggal, Studio_F FROM tbl_pesan WHERE id_pesan = ?";
        String sql = "SELECT * FROM tbl_pesan ORDER BY id_pesan DESC LIMIT 1";
        java.sql.PreparedStatement pst = conn.prepareStatement(sql);
        //pst.setString(1, no);
        ResultSet rs = pst.executeQuery();

        if (rs.next()) {
            txtIDPesan.setText(rs.getString(1));
            jamTayang.setText(rs.getString(2));
            film.setText(rs.getString(3));
            hari.setText(rs.getString(4));
            jumlahTiket.setText(rs.getString(5));
            kursi.setText(rs.getString(6));
            harga.setText(rs.getString(7));
            tanggal.setText(rs.getString(8));
            studio.setText(rs.getString(9));
            cetak();
        } else {
            // Tampilkan pesan jika data tidak ditemukan
            JOptionPane.showMessageDialog(null, "Data tidak ditemukan");
            bersih();

        }
    } catch (SQLException e) {
        e.printStackTrace();
    }
}


